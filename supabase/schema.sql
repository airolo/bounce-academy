create or replace function public.is_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.profiles
    where id = auth.uid()
      and role = 'admin'
  );
$$;

create or replace function public.adjust_product_stock(p_product_id bigint, p_delta integer)
returns integer
language plpgsql
security definer
set search_path = public
as $$
declare
  current_stock integer;
  updated_stock integer;
begin
  if auth.uid() is null then
    raise exception 'Unauthorized';
  end if;

  select stock
  into current_stock
  from public.products
  where id = p_product_id
  for update;

  if current_stock is null then
    raise exception 'Product not found';
  end if;

  updated_stock := current_stock + p_delta;

  if updated_stock < 0 then
    raise exception 'Not enough stock available for this product';
  end if;

  update public.products
  set stock = updated_stock
  where id = p_product_id;

  return updated_stock;
end;
$$;

revoke all on function public.adjust_product_stock(bigint, integer) from public;
grant execute on function public.adjust_product_stock(bigint, integer) to authenticated;

