create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  full_name text,
  email text,
  role text not null default 'user' check (role in ('user', 'admin'))
);

create table if not exists public.products (
  id bigint generated by default as identity primary key,
  name text not null,
  description text not null default '',
  price numeric(10,2) not null check (price >= 0),
  image_url text not null,
  category text not null check (category in ('Hoodie', 'Shirt', 'Short')),
  sizes text[] not null default array['XS','S','M','L','XL']::text[] check (sizes <@ array['XS','S','M','L','XL']::text[]),
  colors text[] not null default array[]::text[],
  color_images jsonb not null default '{}'::jsonb,
  stock integer not null default 0 check (stock >= 0),
  is_featured boolean not null default false
);

alter table public.products add column if not exists description text not null default '';
alter table public.products add column if not exists sizes text[] not null default array['XS','S','M','L','XL']::text[];
alter table public.products add column if not exists colors text[] not null default array[]::text[];
alter table public.products add column if not exists color_images jsonb not null default '{}'::jsonb;
alter table public.products add column if not exists is_featured boolean not null default false;

alter table public.products drop constraint if exists products_category_check;
alter table public.products
add constraint products_category_check check (category in ('Hoodie', 'Shirt', 'Short'));

alter table public.products drop constraint if exists products_sizes_check;

update public.products
set sizes = (
  select coalesce(array_agg(mapped_size), array['XS','S','M','L','XL']::text[])
  from (
    select case
      when lower(trim(size_value)) = 'xs' then 'XS'
      when lower(trim(size_value)) = 's' then 'S'
      when lower(trim(size_value)) = 'm' then 'M'
      when lower(trim(size_value)) = 'l' then 'L'
      when lower(trim(size_value)) = 'xl' then 'XL'
      else null
    end as mapped_size
    from unnest(coalesce(public.products.sizes, array[]::text[])) as size_value
  ) normalized
  where mapped_size is not null
);

alter table public.products
add constraint products_sizes_check check (sizes <@ array['XS','S','M','L','XL']::text[]);

create table if not exists public.orders (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  total_price numeric(10,2) not null check (total_price >= 0),
  status text not null default 'pending' check (status in ('pending', 'processing', 'shipped', 'delivered')),
  shipping_details jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

alter table public.orders alter column shipping_details set default '{}'::jsonb;
update public.orders set shipping_details = '{}'::jsonb where shipping_details is null;
alter table public.orders alter column shipping_details set not null;

alter table public.orders drop constraint if exists orders_shipping_details_check;
alter table public.orders
add constraint orders_shipping_details_check check (
  jsonb_typeof(shipping_details) = 'object'
  and shipping_details ? 'fullName'
  and shipping_details ? 'address'
  and shipping_details ? 'contactNumber'
  and shipping_details ? 'paymentMethod'
  and coalesce(btrim(shipping_details ->> 'fullName'), '') <> ''
  and coalesce(btrim(shipping_details ->> 'address'), '') <> ''
  and coalesce(btrim(shipping_details ->> 'contactNumber'), '') <> ''
  and lower(coalesce(shipping_details ->> 'paymentMethod', '')) = 'cod'
);

